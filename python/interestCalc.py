"""
Simple interest formula is given by:
Simple Interest = (P x T x R)/100
Where,
P is the principle amount
T is the time and
R is the rate
"""
# We can change values here for 
# different inputs 
P = 1 
R = 1 
T = 1 
  
# Calculates simple interest  
SI = (P * R * T) / 100
  
